$(document).ready(function(){
	var $target=$('#privacy li:nth-child(1)')
	
    $('#p1').mouseenter(function(){
        $target.css('color','DarkOrange');
    });
	$('#p1').mouseleave(function(){
        $target.css('color','black');
	});
	
	$('#p1').click(function() {
        $target.toggleClass('highlighted');
    });
	
});


